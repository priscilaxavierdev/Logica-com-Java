public class DeclarandoNumeros {
    public static void main(String[] args){
        int primeiroNumero = 5;
        if(primeiroNumero >= 5){
            System.out.println("Este número é positivo: " + primeiroNumero);
        } else{

            System.out.println("Este número é negativo: " + primeiroNumero);
        }
    }

}
