package academy.devdojo.lacosderepeticao.fors;

public class Aula01IntroducaoFors {
    public static void main(String[] args) {
        for(int contador = 0; contador <= 10; contador = contador + 1){
            System.out.println(contador);
        }
    }
}
/* for = para
 1 - inicializar a variavel
 2 - fazer a comparação
 3 - como você quer incrementar a variável
 contador++ é igual a "contador = contador + 1"
 para, essa variavel contando a partir do 0, sendo menor ou igual a 10, incremente de 1 em 1. faça o comando abaixo
- O laço for é bem mais simplificado do que se ussase o while por colocar mais de um comando é uma só linha
*/